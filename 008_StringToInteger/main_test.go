package main

import "testing"

func TestMyAtoi(t *testing.T) {
	tcs := []struct {
		inStr  string
		outInt int
	}{
		{"42", 42},
		{"   -42", -42},
		{"4193 with words", 4193},
		{"words and 987", 0},
		{"-91283472332", -2147483648},

		{"", 0},
		{"   ", 0},
		{"   - ", 0},
		{"+-", 0},
		{"   --0 ", 0},
		{"++7", 0},
		{"   +0 ", 0},
		{"   +0 1", 0},
		{"   +01", 1},
		{"   +1-", 1},
		{"   1 23", 1},
		{"   1+23", 1},
		{"   -1+23", -1},
		{"   -1 23", -1},
		{"   -1 asd", -1},
		{"-1+", -1},
		{"   a1+23", 0},
		{"  -12 07asd", -12},
		{"  -12+07asd", -12},
		{"  -12t07asd", -12},
		{"  -1207asd", -1207},
		{"  -1207 asd", -1207},
		{"  -1207-asd", -1207},
		{"  -1207+asd", -1207},
		{"q-1207+asd", 0},
		{"q -1207+ asd", 0},
		{"   -123456789987654321+ 123 asd", -2147483648},
		{"   -2147483648+ 123 asd", -2147483648},
		{"  2147483647zzz+ 234 asd", 2147483647},
		{"  +2147483647 123 zzz+ 234 asd", 2147483647},
	}

	for idx, tc := range tcs {
		out := myAtoi(tc.inStr)
		if out != tc.outInt {
			t.Errorf("%d. myAtoi(%q) = %d. Expected: %d", idx, tc.inStr, out, tc.outInt)
		}
	}
}
